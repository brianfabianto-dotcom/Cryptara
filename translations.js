let currentLang = localStorage.getItem('cryptaraLang') || 'id';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('cryptaraLang', lang);

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const keys = el.getAttribute('data-i18n').split('.');
        let text = translations[lang];
        for (let key of keys) {
            if (text) text = text[key];
            else break;
        }
        if (text !== undefined && text !== null) {
            if (el.hasAttribute('data-i18n-html')) {
                el.innerHTML = text;
            } else if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.placeholder = text;
            } else {
                el.textContent = text;
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const keys = el.getAttribute('data-i18n-placeholder').split('.');
        let text = translations[lang];
        for (let key of keys) {
            if (text) text = text[key];
            else break;
        }
        if (text !== undefined && text !== null) {
            el.placeholder = text;
        }
    });

    const langBtn = document.getElementById('langToggle');
    if (langBtn) langBtn.textContent = lang === 'id' ? 'EN' : 'ID';
}

function t(key) {
    const keys = key.split('.');
    let text = translations[currentLang];
    for (let k of keys) {
        if (text) text = text[k];
        else return key;
    }
    return text !== undefined && text !== null ? text : key;
}

function initLanguage() {
    setLanguage(currentLang);
    const langBtn = document.getElementById('langToggle');
    if (langBtn && !langBtn.hasAttribute('data-lang-initialized')) {
        langBtn.setAttribute('data-lang-initialized', 'true');
        langBtn.addEventListener('click', () => {
            const newLang = currentLang === 'id' ? 'en' : 'id';
            setLanguage(newLang);
            if (typeof refreshPageContent === 'function') refreshPageContent();
        });
    }
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLanguage);
} else {
    initLanguage();
}